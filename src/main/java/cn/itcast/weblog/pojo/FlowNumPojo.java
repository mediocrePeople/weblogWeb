package cn.itcast.weblog.pojo;

public class FlowNumPojo {

    private String id;
    private String dateStr;
    private String pVNum;
    private Long uVNum;
    private String iPNum;
    private Long newUvNum;
    private String visitNum;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDateStr() {
        return dateStr;
    }

    public void setDateStr(String dateStr) {
        this.dateStr = dateStr;
    }

    public String getpVNum() {
        return pVNum;
    }

    public void setpVNum(String pVNum) {
        this.pVNum = pVNum;
    }

    public Long getuVNum() {
        return uVNum;
    }

    public void setuVNum(Long uVNum) {
        this.uVNum = uVNum;
    }

    public String getiPNum() {
        return iPNum;
    }

    public void setiPNum(String iPNum) {
        this.iPNum = iPNum;
    }

    public Long getNewUvNum() {
        return newUvNum;
    }

    public void setNewUvNum(Long newUvNum) {
        this.newUvNum = newUvNum;
    }

    public String getVisitNum() {
        return visitNum;
    }

    public void setVisitNum(String visitNum) {
        this.visitNum = visitNum;
    }
}
